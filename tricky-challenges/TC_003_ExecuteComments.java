/* 
    Program: Demonstrate how we can execute comments in Java with an example
*/
class TC_003_ExecuteComments
{
    public static void main(String[] args)
    {
        // Unicode character of Carriage Return (CR) / New line = \u000d
        // First line of comment \u000d System.out.println("Hello World");

        int value = 10;
        // \u000d value = 20;
        System.out.println("value = " + value);
    }
}
